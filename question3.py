#Tuple of sisters
sisters=('Nithisha','Deepika')
#Tuple of brothers
brothers=('Vivek','Goutham','Ashok')
#Joining sisters and brothers tuples and assigning to siblings
siblings=sisters+brothers
print("siblings are:",siblings)
#How many siblings do you have
print("count of siblings:",len(siblings))
#modifying siblings by adding mother and father
parents=('kalyani','lavakumar')
#Adding siblings and parents to family members
family_members=siblings+parents
print("Family members are:",family_members)